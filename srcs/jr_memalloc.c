#include "libjr.h"

void	*jr_memalloc(size_t size)
{
	void	*ptr;
	char	*temp;

	ptr = malloc(size);
	if (!ptr)
		return (NULL);
	temp = (char *)ptr;
	while (size--)
		*temp++ = 0;
	return (ptr);
}
