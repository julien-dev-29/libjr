#include "libjr.h"

int	jr_atoi(char *nptr)
{
	size_t	i;
	int		res;

	i = 0;
	res = 0;
	while (nptr[i])
	{
		res *= 10;
		if (!(nptr[i] >= 48 && nptr[i] <= 57))
			return (0);
		res += nptr[i] - '0';
		i++;
	}
	return (res);
}
