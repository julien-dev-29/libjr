#include "libjr.h"

void	jr_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 1);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
		jr_putnbr_fd(n / 10, fd);
	write(fd, &"0123456789"[n % 10], 1);
}
